X , Y = input().split(" ")
X = float(X)
Y = float(Y)
if X == 0 != Y:
    print("Eixo Y")
elif Y == 0 != X:
    print("Eixo X")
elif X > 0 < Y:
    print("Q1")
elif X < 0 < Y:
    print("Q2")
elif X < 0 > Y:
    print("Q3")
elif X > 0 > Y:
    print("Q4")
else:
    print("Origem")
